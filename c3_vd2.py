#các ký tự thứ hai tính từ cuối lên (lấy) đến hêt
word='python'
print('word[-2:]:', word[2:])
# các ký tự từ vì trí thứ 4 (lấy)đến hết
print('word[4:]:',word[4:])
# các ký tự từ ký tự thứ hai đến cuối
print('work[2:]:',word[2:])
print('word[:4]+word[4:]',word[:4]+word[4:])
# các ký tự từ đầu đến vị trí thứ 2'
print('word[:2]', word[:2])
# các ký tự vị trí 2 đến 5
print('word=[2:5]',word[2:5])
